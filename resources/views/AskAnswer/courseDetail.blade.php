                            </div>
                            <div class="col-12 cd-body text-justify">
                                <p>
                                The full form of BCA is Bachelor of Computer Application. It enables you to get both practcal and theoretical hands-on training in computer applications
                                and software engineering. The course is for those students who are interested in the computer science field and wish to gain knowledge in various aspects 
                                of computer applications like networking, software engineering, hardware engineering, website designing, etc. BCA provides and demonstrates in-depth knowledge
                                in technical areas of industrial computing systems, which easily leads skills required to meet the challenges of the IT world. Some of the common
                                BCA subjects include Discrete Mathematics, Database Management Systems, Digital Computer Programming, Computer Networks, Cloud Computing, etc. The course requires students to learn different important and latest software languages like JAVA, Oracle, C++, Python and alike, which are huge in demand in the tech industry.
